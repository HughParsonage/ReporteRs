package org.lysis.reporters.pptx4r.tools;

public class Utils {
	public static String replaceAcutesHTML(String str) {

		str = str.replaceAll("&", "&amp;");
		str = str.replaceAll("<", "&lt;");
		str = str.replaceAll(">", "&gt;");
		str = str.replaceAll("\n", "");

		return str;
		}
}
